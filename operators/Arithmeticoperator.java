public class Arithmeticoperator {
    
    public static void main(String[] args){
        int a=15;
        int b=10;
        
        System.out.println(a+b); //25
        System.out.println(a-b); //5
        System.out.println(a*b); //150
        System.out.println(a/b); //1
        System.out.println(a%b); //5
    }
}
