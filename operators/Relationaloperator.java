class Relationaloperator{
    public static void main(String[] args ){
        // create variable 
        int a=6, b=12;

        // value of a and b
        System.out.println("a is " + a + "and b is " + b);
        // == operator
        System.out.println(a==b);  //false
        // != operator
        System.out.println(a!=b); //true
        // > operator
        System.out.println(a>b); // false
        // < operator
        System.out.println(a<b); // true
        // >= operator
        System.out.println(a>=b); // false
        // <= operator
        System.out.println(a<=b); // true
    }
}